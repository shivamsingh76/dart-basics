void main() {
  // Data Types in Dart
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // Strings
  String firstName = "John";
  print("String: $firstName");

  // Intergers
  int myNum = 40;
  print("Integer: $myNum");

  // Doubles
  double myDouble = 40.5;
  print("Double: $myDouble");

  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = "John Doe";
  print("Dynamic : $fullName");
}
